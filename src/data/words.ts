export const WORD_CATEGORIES_KEYS = Object.keys(WORD_CATEGORIES) as Array<keyof typeof WORD_CATEGORIES>

export function getRandomWord(category: keyof typeof WORD_CATEGORIES, usedWords: Set<string>): string {
  const words = WORD_CATEGORIES[category]
  const availableWords = words.filter(word => !usedWords.has(word))
  
  if (availableWords.length === 0) {
    // Si agotamos las palabras, resetear el set
    return words[Math.floor(Math.random() * words.length)]
  }
  
  return availableWords[Math.floor(Math.random() * availableWords.length)]
}
